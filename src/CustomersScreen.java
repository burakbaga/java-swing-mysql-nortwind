    public void showCustomers() {

        model.setRowCount(0);

        ArrayList<Customers> customers = co.getOperations();

        if (customers != null) {
            for (Customers c : customers) {

                Object[] add = {c.getId(), c.getCompanyName(), c.getContactName(), c.getContactTitle(), c.getAddress(), c.getCity(), c.getRegion(),
                    c.getPostalCode(), c.getCountry(), c.getPhone(), c.getFax()};

                model.addRow(add);
            }

        }

    }

