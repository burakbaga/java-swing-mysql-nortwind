    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        messageLabel.setText("");

        String id = customerIdField.getText();
        String companyName = companyNameField.getText();
        String contactName = contactNameField.getText();
        String contactTitle = contactTitleField.getText();
        String Address = addressField.getText();
        String city = cityField.getText();
        String region = regionField.getText();
        String postalCode = postalField.getText();
        String country = countryField.getText();
        String phone = phoneField.getText();
        String Fax = faxField.getText();

        if (id.length() == 5) {
            co.addCustomer(id, companyName, contactName, contactTitle, Address, city, region, postalCode, country, phone, Fax);
            messageLabel.setText("record successfully added.");
        } else {
            messageLabel.setText("Please enter correct Id. (must be 5) ");
        }

        showCustomers();

    }//GEN-LAST:event_addButtonActionPerformed
    public void dinamicSearch(String search) {

        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);

        customerTable.setRowSorter(tr);

        tr.setRowFilter(RowFilter.regexFilter(search));

    }

    private void searchFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchFieldKeyReleased

        String search = searchField.getText();

        dinamicSearch(search);


    }//GEN-LAST:event_searchFieldKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        messageLabel.setText("");
        int selectedRow = customerTable.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {

                messageLabel.setText("There are no records in the table.");

            } else {

                messageLabel.setText("Please select a row.");
            }
        } else {

            String id = (String) model.getValueAt(selectedRow, 0);

            co.deleteCustomer(id);

            messageLabel.setText("The record has been deleted.");
            showCustomers();

        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed

        String companyName = companyNameField.getText();
        String contactName = contactNameField.getText();
        String contactTitle = contactTitleField.getText();
        String Address = addressField.getText();
        String city = cityField.getText();
        String region = regionField.getText();
        String postalCode = postalField.getText();
        String country = countryField.getText();
        String phone = phoneField.getText();
        String Fax = faxField.getText();

        int selectedRow = customerTable.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {

                messageLabel.setText("There are no records in the table.");

            } else {

                messageLabel.setText("Please select a row.");
            }
        } else {
            String id = (String) model.getValueAt(selectedRow, 0);

            co.updateCustomer(id, companyName, contactName, contactTitle, Address, city, region, postalCode, country, phone, Fax);

            messageLabel.setText("Record was successfully updated");

            showCustomers();
        }


    }//GEN-LAST:event_updateButtonActionPerformed

